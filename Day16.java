public class Day16 {
    public static void main(String[] args) {

        int a = 20;
        int b = 10;

        // operator aritmatika perkalian *
        int hasilKali = a * b;
        // operator aritmatika pembagian /
        int hasilBagi = a / b;

        // ouputnya
        System.out.println("Hasil perkalian : " + hasilKali); // hasilnya 200
        System.out.println("Hasil pembagian : " + hasilBagi); // hasilnya 2
    }
}

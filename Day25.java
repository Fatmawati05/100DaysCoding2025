public class Day25 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("i = " + i); // hasilnya 1 sampai 10
        }
        System.out.println(" ");

        for (int j = 10; j >= 1; j--) {
            System.out.println("j = " + j); // hasilnya 10 sampai 1

        }
    }
}

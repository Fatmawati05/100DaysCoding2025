public class Day15 {
    public static void main(String[] args) {

        int a = 20;
        int b = 16;

        // operator aritmatika penjumlahan +
        int hasilJumlah = a + b;
        // operator aritmatika pengurangan -
        int hasilKurang = a - b;

        // ouputnya
        System.out.println("Hasil penjumlahan : " + hasilJumlah); // hasilnya 36
        System.out.println("Hasil pengurangan : " + hasilKurang); // hasilnya 4
    }
}
